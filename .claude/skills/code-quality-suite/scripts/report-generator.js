#!/usr/bin/env node

/**
 * Report Generator
 * コード品質・セキュリティ・パフォーマンスの統合レポートを生成
 */

const fs = require('fs');
const path = require('path');

/**
 * 統合レポートを生成
 * @param {Object} qualityResults - 品質チェック結果
 * @param {Object} securityResults - セキュリティスキャン結果
 * @param {Object} options - レポートオプション
 * @returns {string} Markdownレポート
 */
function generateReport(qualityResults, securityResults, options = {}) {
  const { includeDetails = true, format = 'markdown' } = options;

  // 総合スコアを計算
  const overallScore = calculateOverallScore(qualityResults, securityResults);

  const sections = [];

  // ヘッダー
  sections.push('# Code Quality Suite Report');
  sections.push(`Generated: ${new Date().toISOString().split('T')[0]}`);
  sections.push('');

  // 総合スコア
  sections.push('## Overall Score');
  sections.push('');
  sections.push(`**${overallScore.score}/100** (${overallScore.rating.toUpperCase()})`);
  sections.push('');
  sections.push('| Category | Score | Rating | Issues |');
  sections.push('|----------|-------|--------|--------|');
  sections.push(`| Code Quality | ${qualityResults.summary.score}/100 | ${qualityResults.summary.rating} | ${qualityResults.summary.totalIssues} |`);
  sections.push(`| Security | ${securityResults.summary.score}/100 | ${securityResults.summary.rating} | ${securityResults.summary.totalIssues} |`);
  sections.push('');

  // コード品質セクション
  sections.push('## Code Quality');
  sections.push('');

  if (qualityResults.linting && qualityResults.linting.status !== 'skipped') {
    sections.push('### ESLint');
    sections.push(`- Errors: ${qualityResults.linting.errorCount}`);
    sections.push(`- Warnings: ${qualityResults.linting.warningCount}`);
    sections.push(`- Files checked: ${qualityResults.linting.files}`);
    sections.push('');
  }

  if (qualityResults.typescript && qualityResults.typescript.status !== 'skipped') {
    sections.push('### TypeScript');
    sections.push(`- Type errors: ${qualityResults.typescript.errorCount}`);
    sections.push('');
  }

  sections.push('### Code Metrics');
  sections.push(`- Total files: ${qualityResults.complexity.fileCount}`);
  sections.push(`- Total lines: ${qualityResults.complexity.totalLines}`);
  sections.push(`- Average lines per file: ${qualityResults.complexity.averageLinesPerFile}`);
  sections.push('');

  // セキュリティセクション
  sections.push('## Security');
  sections.push('');

  if (securityResults.dependencies && securityResults.dependencies.status !== 'skipped') {
    sections.push('### Dependency Vulnerabilities');
    const vuln = securityResults.dependencies.vulnerabilities;
    sections.push(`- Critical: ${vuln.critical}`);
    sections.push(`- High: ${vuln.high}`);
    sections.push(`- Moderate: ${vuln.moderate}`);
    sections.push(`- Low: ${vuln.low}`);
    sections.push(`- Total: ${securityResults.dependencies.totalVulnerabilities}`);
    sections.push('');

    if (securityResults.dependencies.totalVulnerabilities > 0) {
      sections.push('> 💡 **Recommendation**: Run `npm audit fix` to automatically fix vulnerabilities');
      sections.push('');
    }
  }

  sections.push('### Secret Detection');
  sections.push(`- Potential secrets found: ${securityResults.secrets.count}`);
  sections.push('');

  if (securityResults.secrets.count > 0 && includeDetails) {
    sections.push('**Detected Secrets:**');
    sections.push('');
    securityResults.secrets.findings.slice(0, 10).forEach((finding) => {
      sections.push(`- **${finding.type}** in \`${finding.file}:${finding.line}\``);
    });
    if (securityResults.secrets.count > 10) {
      sections.push(`- ... and ${securityResults.secrets.count - 10} more`);
    }
    sections.push('');
  }

  // 推奨事項
  sections.push('## Recommendations');
  sections.push('');

  const recommendations = generateRecommendations(qualityResults, securityResults);
  if (recommendations.length > 0) {
    recommendations.forEach((rec, index) => {
      sections.push(`${index + 1}. **[${rec.priority.toUpperCase()}]** ${rec.description}`);
    });
  } else {
    sections.push('✅ No critical issues found!');
  }
  sections.push('');

  // アクションアイテム
  sections.push('## Action Items');
  sections.push('');
  const actions = generateActionItems(qualityResults, securityResults);
  if (actions.length > 0) {
    actions.forEach((action, index) => {
      sections.push(`- [ ] ${action}`);
    });
  } else {
    sections.push('- [x] All checks passed!');
  }
  sections.push('');

  // フッター
  sections.push('---');
  sections.push('*Generated by Code Quality Suite Agent Skill*');
  sections.push('');

  return sections.join('\n');
}

/**
 * 総合スコアを計算
 * @param {Object} qualityResults - 品質結果
 * @param {Object} securityResults - セキュリティ結果
 * @returns {Object} 総合スコア
 */
function calculateOverallScore(qualityResults, securityResults) {
  // 品質とセキュリティの重み付け平均
  const qualityWeight = 0.5;
  const securityWeight = 0.5;

  const score = Math.round(
    qualityResults.summary.score * qualityWeight +
    securityResults.summary.score * securityWeight
  );

  let rating = 'excellent';
  if (score < 50) rating = 'poor';
  else if (score < 70) rating = 'fair';
  else if (score < 85) rating = 'good';

  return {
    score,
    rating,
  };
}

/**
 * 推奨事項を生成
 * @param {Object} qualityResults - 品質結果
 * @param {Object} securityResults - セキュリティ結果
 * @returns {Array} 推奨事項
 */
function generateRecommendations(qualityResults, securityResults) {
  const recommendations = [];

  // セキュリティの重大な問題
  if (securityResults.summary.criticalIssues > 0) {
    recommendations.push({
      priority: 'critical',
      description: `Fix ${securityResults.summary.criticalIssues} critical security vulnerabilities immediately`,
    });
  }

  // 品質の問題
  if (qualityResults.linting && qualityResults.linting.errorCount > 0) {
    recommendations.push({
      priority: 'high',
      description: `Resolve ${qualityResults.linting.errorCount} ESLint errors`,
    });
  }

  if (qualityResults.typescript && qualityResults.typescript.errorCount > 0) {
    recommendations.push({
      priority: 'high',
      description: `Fix ${qualityResults.typescript.errorCount} TypeScript type errors`,
    });
  }

  // セキュリティの中程度の問題
  if (securityResults.summary.highIssues > 0) {
    recommendations.push({
      priority: 'medium',
      description: `Address ${securityResults.summary.highIssues} high-priority security issues`,
    });
  }

  // シークレット検出
  if (securityResults.secrets.count > 0) {
    recommendations.push({
      priority: 'high',
      description: `Review and remove ${securityResults.secrets.count} potential secrets from codebase`,
    });
  }

  // 品質の警告
  if (qualityResults.linting && qualityResults.linting.warningCount > 10) {
    recommendations.push({
      priority: 'low',
      description: `Address ${qualityResults.linting.warningCount} ESLint warnings to improve code quality`,
    });
  }

  return recommendations.sort((a, b) => {
    const priorityOrder = { critical: 0, high: 1, medium: 2, low: 3 };
    return priorityOrder[a.priority] - priorityOrder[b.priority];
  });
}

/**
 * アクションアイテムを生成
 * @param {Object} qualityResults - 品質結果
 * @param {Object} securityResults - セキュリティ結果
 * @returns {Array} アクションアイテム
 */
function generateActionItems(qualityResults, securityResults) {
  const actions = [];

  if (qualityResults.linting && qualityResults.linting.errorCount > 0) {
    actions.push(`Run \`npm run lint -- --fix\` to auto-fix ESLint issues`);
  }

  if (securityResults.dependencies && securityResults.dependencies.totalVulnerabilities > 0) {
    actions.push(`Run \`npm audit fix\` to update vulnerable dependencies`);
  }

  if (securityResults.secrets.count > 0) {
    actions.push(`Review detected secrets and move to environment variables`);
  }

  if (qualityResults.typescript && qualityResults.typescript.errorCount > 0) {
    actions.push(`Fix TypeScript compilation errors`);
  }

  return actions;
}

/**
 * レポートをファイルに保存
 * @param {string} report - レポート内容
 * @param {string} outputPath - 出力パス
 */
function saveReport(report, outputPath) {
  const dir = path.dirname(outputPath);
  if (!fs.existsSync(dir)) {
    fs.mkdirSync(dir, { recursive: true });
  }

  fs.writeFileSync(outputPath, report);
  console.log(`\n✓ Report saved to: ${outputPath}\n`);
}

/**
 * CLIエントリーポイント
 */
async function main() {
  const reportsDir = path.join(__dirname, '..', 'reports');

  // 最新の品質とセキュリティレポートを読み込む
  if (!fs.existsSync(reportsDir)) {
    console.error('❌ Error: No reports directory found');
    console.log('Run quality-checker.js and security-scanner.js first');
    process.exit(1);
  }

  const files = fs.readdirSync(reportsDir);
  const qualityFile = files.find((f) => f.startsWith('quality-check-'));
  const securityFile = files.find((f) => f.startsWith('security-scan-'));

  if (!qualityFile || !securityFile) {
    console.error('❌ Error: Missing quality or security reports');
    console.log('Run quality-checker.js and security-scanner.js first');
    process.exit(1);
  }

  const qualityResults = JSON.parse(
    fs.readFileSync(path.join(reportsDir, qualityFile), 'utf8')
  );
  const securityResults = JSON.parse(
    fs.readFileSync(path.join(reportsDir, securityFile), 'utf8')
  );

  // レポートを生成
  const report = generateReport(qualityResults, securityResults);

  // 出力
  const timestamp = new Date().toISOString().split('T')[0];
  const outputPath = path.join(reportsDir, `code-quality-suite-${timestamp}.md`);
  saveReport(report, outputPath);

  console.log('📊 Code Quality Suite Report Summary:');
  const overallScore = calculateOverallScore(qualityResults, securityResults);
  console.log(`   Overall Score: ${overallScore.score}/100 (${overallScore.rating})`);
  console.log(`   Quality: ${qualityResults.summary.score}/100`);
  console.log(`   Security: ${securityResults.summary.score}/100\n`);
}

// スクリプトとして実行された場合
if (require.main === module) {
  main();
}

module.exports = { generateReport, calculateOverallScore, saveReport };
